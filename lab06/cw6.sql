CREATE OR REPLACE FUNCTION f_formatuj_telefon(p_telefon IN VARCHAR2) RETURN VARCHAR2 IS
BEGIN
    IF LENGTH(p_telefon) >= 3 THEN
        RETURN '(' || SUBSTR(p_telefon, 1, 3) || ')' || SUBSTR(p_telefon, 4);
    ELSE
        RETURN p_telefon;
    END IF;
END f_formatuj_telefon;
