CREATE OR REPLACE FUNCTION f_statystyki_kraju(
    p_kraj IN VARCHAR2,
    p_liczba_prac OUT NUMBER,
    p_liczba_dep OUT NUMBER
) RETURN BOOLEAN IS
    v_country_id countries.country_id%TYPE;
BEGIN
    SELECT country_id INTO v_country_id FROM countries WHERE UPPER(country_name) = UPPER(p_kraj);
    SELECT COUNT(DISTINCT d.department_id), COUNT(e.employee_id)
    INTO p_liczba_dep, p_liczba_prac
    FROM locations l
    LEFT JOIN departments d ON l.location_id = d.location_id
    LEFT JOIN employees e ON d.department_id = e.department_id
    WHERE l.country_id = v_country_id;
    
    RETURN TRUE;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20001, 'Kraj o podanej nazwie nie istnieje w bazie.');
END f_statystyki_kraju;
