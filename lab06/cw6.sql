CREATE OR REPLACE FUNCTION f_dziwna_wielkosc(p_tekst IN VARCHAR2) RETURN VARCHAR2 IS
    v_dlugosc NUMBER := LENGTH(p_tekst);
BEGIN
    IF p_tekst IS NULL THEN RETURN NULL; END IF;
    IF v_dlugosc = 1 THEN RETURN UPPER(p_tekst); END IF;
    
    RETURN UPPER(SUBSTR(p_tekst, 1, 1)) || 
           LOWER(SUBSTR(p_tekst, 2, v_dlugosc - 2)) || 
           UPPER(SUBSTR(p_tekst, -1));
END f_dziwna_wielkosc;
/
