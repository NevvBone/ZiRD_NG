CREATE OR REPLACE FUNCTION f_pesel_na_date(p_pesel IN VARCHAR2) RETURN DATE IS
    v_rok VARCHAR2(4);
    v_miesiac VARCHAR2(2);
    v_dzien VARCHAR2(2);
    v_miesiac_num NUMBER;
BEGIN
    IF LENGTH(p_pesel) != 11 THEN RETURN NULL; END IF;
    
    v_rok := SUBSTR(p_pesel, 1, 2);
    v_miesiac_num := TO_NUMBER(SUBSTR(p_pesel, 3, 2));
    v_dzien := SUBSTR(p_pesel, 5, 2);
    
    IF v_miesiac_num > 20 THEN
        v_rok := '20' || v_rok;
        v_miesiac := TO_CHAR(v_miesiac_num - 20, 'FM00');
    ELSE
        v_rok := '19' || v_rok;
        v_miesiac := TO_CHAR(v_miesiac_num, 'FM00');
    END IF;
    
    RETURN TO_DATE(v_rok || '-' || v_miesiac || '-' || v_dzien, 'YYYY-MM-DD');
END f_pesel_na_date;
/
