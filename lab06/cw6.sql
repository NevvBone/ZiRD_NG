CREATE OR REPLACE FUNCTION f_nazwa_pracy(p_job_id IN VARCHAR2) RETURN VARCHAR2 IS
    v_title jobs.job_title%TYPE;
BEGIN
    SELECT job_title INTO v_title FROM jobs WHERE job_id = p_job_id;
    RETURN v_title;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN 'Błąd: Praca o podanym ID nie istnieje';
END f_nazwa_pracy;
