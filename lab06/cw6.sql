CREATE OR REPLACE PACKAGE pkg_regions_crud IS
    PROCEDURE dodaj_region(p_id NUMBER, p_nazwa VARCHAR2);
    PROCEDURE aktualizuj_region(p_id NUMBER, p_nowa_nazwa VARCHAR2);
    PROCEDURE usun_region(p_id NUMBER);
    FUNCTION pobierz_nazwe_regionu(p_id NUMBER) RETURN VARCHAR2;
END pkg_regions_crud;
/
CREATE OR REPLACE PACKAGE BODY pkg_regions_crud IS
    PROCEDURE dodaj_region(p_id NUMBER, p_nazwa VARCHAR2) IS
    BEGIN
        INSERT INTO regions (region_id, region_name) VALUES (p_id, p_nazwa);
        COMMIT;
    END dodaj_region;
    PROCEDURE aktualizuj_region(p_id NUMBER, p_nowa_nazwa VARCHAR2) IS
    BEGIN
        UPDATE regions SET region_name = p_nowa_nazwa WHERE region_id = p_id;
        COMMIT;
    END aktualizuj_region;
    PROCEDURE usun_region(p_id NUMBER) IS
    BEGIN
        DELETE FROM regions WHERE region_id = p_id;
        COMMIT;
    END usun_region;
    FUNCTION pobierz_nazwe_regionu(p_id NUMBER) RETURN VARCHAR2 IS
        v_nazwa regions.region_name%TYPE;
    BEGIN
        SELECT region_name INTO v_nazwa FROM regions WHERE region_id = p_id;
        RETURN v_nazwa;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN RETURN 'Region nie istnieje';
    END pobierz_nazwe_regionu;
END pkg_regions_crud;
