    czas_zmiany DATE
);
CREATE OR REPLACE TRIGGER trg_widelek_pensji
BEFORE INSERT OR UPDATE OF salary ON employees
FOR EACH ROW
DECLARE
    PRAGMA AUTONOMOUS_TRANSACTION; 
BEGIN
    IF :NEW.salary < 2000 OR :NEW.salary > 26000 THEN
        INSERT INTO zlodziej (uzytkownik, czas_zmiany) VALUES (USER, SYSDATE);
        COMMIT;
        RAISE_APPLICATION_ERROR(-20002, 'Niedozwolone wynagrodzenie! Akcja zalogowana.');
    END IF;
END;
